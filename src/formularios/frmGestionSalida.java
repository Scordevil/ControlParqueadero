    private void cargaModelo() {
        List<GestionBean> obj = GestionDBController.getAllReg(0);
        if (obj != null) {
            if (obj.size() != 0) {

                for (GestionBean value : obj) {
                    Vector<String> v = new Vector<>();
                    v.add("" + value.getRegistro_id());
                    v.add("" + value.getAuto_placa());
                    v.add("" + value.getAuto_marca());
                    v.add("" + value.getAuto_modelo());
                    v.add("" + value.getAuto_color());
                    v.add("" + value.getFecha_reg_entrada());

                    modeloTabla.addRow(v);
                }

            }
        }
    }

    public void limpiar() {
        int filas = modeloTabla.getRowCount();
        if (filas > 0) {
            for (int i = 0; i < filas; i++) {
                modeloTabla.removeRow(0);
            }
        }
    }

