    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int fsel = jTable1.getSelectedRow();
        if (fsel == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un registro");
        } else {
            registro_id = Integer.valueOf(jTable1.getValueAt(fsel, 0).toString());
            jtf_fecha_salida.setText(TiempoRegistroBean.getTiempoReg());
            jtf_fecha_entrada.setText(jTable1.getValueAt(fsel, 5).toString());
            ResultTimeBean obje = Utils.DiferenciaFechas(jtf_fecha_entrada.getText(), jtf_fecha_salida.getText());

            horas = obje.getHoras();
            minutos = obje.getMinutos();

            jtf_total_horas.setText("" + obje.getHoras());
            jtf_total_minutos.setText("" + obje.getMinutos());
        }

        jtf_total_pago.setText("" + getTotalPagar(horas, minutos));


    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        GestionBean obj = new GestionBean(
                registro_id,
                jtf_fecha_salida.getText().toString(),
                Integer.valueOf(jtf_total_horas.getText().toString()),
                Integer.valueOf(jtf_total_minutos.getText().toString()),
                Double.valueOf(jtf_total_pago.getText().toString()),
                1);
        boolean update = GestionDBController.setGestion(obj);

        if (update) {
            JOptionPane.showMessageDialog(rootPane, "Registro de salida procesado");
            registro_id = 0;
            jtf_fecha_salida.setText("");
            jtf_fecha_entrada.setText("");
            horas = 0;
            minutos = 0;
            jtf_total_horas.setText("");
            jtf_total_minutos.setText("");
            jtf_total_pago.setText("");
            limpiar();
            cargaModelo();

        } else {
            JOptionPane.showMessageDialog(rootPane, "No se logro realizar la operacion, contacte al administrador");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public double getTotalPagar(long hor, long min) {
        double totalPago = 0.00;
        //Pregunto si la horas son igual a 0 o menos a 12 horas
        if (hor >= 0 && hor < 12) {
            totalPago = ((hor * 60) * Double.valueOf(parametro.getCosto_min()) + (min * Double.valueOf(parametro.getCosto_min())));
            if (totalPago > Double.valueOf(parametro.getCosto_max_hora())) {
                totalPago = Double.valueOf(parametro.getCosto_max_hora());
            }
        } else {
            if (hor >= 12 && hor < 24) {
                long horDif = hor - 12L;
                if (horDif <= 12) {
                    totalPago = ((Double.valueOf(parametro.getCosto_max_hora())) + ((horDif * 60) * Double.valueOf(parametro.getCosto_min())) + ((min * Double.valueOf(parametro.getCosto_min()))));
                    if (totalPago > (Double.valueOf(parametro.getCosto_max_hora())) * 2) {
                        totalPago = (Double.valueOf(parametro.getCosto_max_hora()) * 2);
                    }
                }
            } else {
                if (hor >= 24 && hor < 36) {
                    long horDif = hor - 24L;
                    if (horDif <= 12) {
                        totalPago = ((Double.valueOf(parametro.getCosto_max_hora()) * 2) + ((horDif * 60) * Double.valueOf(parametro.getCosto_min()) + (min * Double.valueOf(parametro.getCosto_min()))));
                        if (totalPago > (Double.valueOf(parametro.getCosto_max_hora())) * 3) {
                            totalPago = (Double.valueOf(parametro.getCosto_max_hora()) * 3);
                        }
                    }

                }else{
                    long horDif = hor - 36L;
                    if (horDif <= 12) {
                        totalPago = ((Double.valueOf(parametro.getCosto_max_hora()) * 3) + ((horDif * 60) * Double.valueOf(parametro.getCosto_min()) + (min * Double.valueOf(parametro.getCosto_min()))));
                        if (totalPago > (Double.valueOf(parametro.getCosto_max_hora())) * 4) {
                            totalPago = (Double.valueOf(parametro.getCosto_max_hora()) * 4);
                        }
                    }else{
                     horDif = hor - 48L;
                    totalPago = (Double.valueOf(parametro.getCosto_max_hora()) * 4)+ ((horDif * 60) * Double.valueOf(parametro.getCosto_min()) + (min * Double.valueOf(parametro.getCosto_min())));
                    }
                }
            }
        }

        return totalPago;
    }

