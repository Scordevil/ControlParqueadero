    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jtf_costo_min.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe indicar la tarifa por minuto");
            jtf_costo_min.requestFocus();
            return;
        } else if (jtf_costo_max.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe indicar la tarifa maxima (12 horas)");
            jtf_costo_max.requestFocus();
            return;
        } else {

            TarifasBean obj = new TarifasBean(jtf_costo_min.getText().toString().trim().replace(",", "."),
                    jtf_costo_max.getText().toString().trim().replace(",", "."));
            switch (typeOpe) {
                case 0:
                    boolean create = TarifasDBController.createTarifa(obj);

                    if (create) {
                        JOptionPane.showMessageDialog(rootPane, "Tarifa Registrada.");
                    }
                    break;
                case 1:
                    obj.setId(id);
                    boolean update = TarifasDBController.setTarifas(obj);

                    if (update) {
                        JOptionPane.showMessageDialog(rootPane, "Tarifa Actualizada.");
                    }
                    break;
            }

            jtf_costo_min.setEnabled(false);
            jtf_costo_max.setEnabled(false);

        }

    }//GEN-LAST:event_jButton3ActionPerformed

