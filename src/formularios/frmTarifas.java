    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened

        TarifasBean obj = TarifasDBController.getTarifa();

        if (obj != null) {
            id = obj.getId();
            jtf_costo_min.setText(obj.getCosto_min().toString().trim());
            jtf_costo_max.setText(obj.getCosto_max_hora().toString().trim());

            jtf_costo_min.setEnabled(false);
            jtf_costo_max.setEnabled(false);

        } else {
            typeOpe = 0;
            JOptionPane.showMessageDialog(rootPane, "No se ha registrado tarifas, Debe registrar las tarifas aplicar.");
        }
    }//GEN-LAST:event_formInternalFrameOpened

