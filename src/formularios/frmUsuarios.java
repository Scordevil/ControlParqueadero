    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (cmbPerfil.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un perfil para\nel usuario a crear. ");
        } else {

            if (txtNombres.getText().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Debe indicar el nombre completo de la persona a registrar");
                txtNombres.requestFocus();
                return;
            } else if (txtApellidos.getText().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Debe indicar el apellido completo de la persona a registrar");
                txtApellidos.requestFocus();
                return;
            } else if (txtIdUsuario.getText().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Debe indicar el nombre de usuario");
                txtIdUsuario.requestFocus();
                return;
            } else if (txtClave.getText().toString().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Debe indicar una contrase√±a");
                txtClave.requestFocus();
                return;
            } else if (txtConfirClave.getText().length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Debe confirmar la contrase√±a");
                txtConfirClave.requestFocus();
                return;
            } else {
                if (txtClave.getText().toString().trim().equals(txtConfirClave.getText().toString().trim())) {
                    if (cmbPerfil.getSelectedIndex() == 1) {
                        typePerfil = 1;
                    } else {
                        typePerfil = 2;
                    }

                    if (chk_status.isSelected() == true) {
                        status = 1;
                    } else {
                        status = 0;
                    }

                    UsuarioBean user = new UsuarioBean(typePerfil,
                            txtNombres.getText().toString().trim(),
                            txtApellidos.getText().toString().trim(),
                            txtIdUsuario.getText().toString().trim(),
                            txtClave.getText().toString().trim(), status);

                    switch (typeOpe) {
                        case 0:
                            if (UserDBController.createUser(user)) {

                                btnNuevo.setEnabled(true);
                                btnEditar.setEnabled(true);
                                btnBorrar.setEnabled(true);
                                btnBuscar.setEnabled(true);
                                btnGuardar.setEnabled(false);
                                btnCancelar.setEnabled(false);

                                //deshabilita los campos
                                txtIdUsuario.setEnabled(false);
                                txtIdUsuario.setText("");
                                txtNombres.setEnabled(false);
                                txtNombres.setText("");
                                txtApellidos.setEnabled(false);
                                txtApellidos.setText("");
                                txtClave.setEnabled(false);
                                txtClave.setText("");
                                txtConfirClave.setEnabled(false);
                                txtConfirClave.setText("");
                                txtIdUsuario.setEnabled(false);
                                txtIdUsuario.setText("");
                                cmbPerfil.setEnabled(false);
                                cmbPerfil.setSelectedIndex(0);
                                chk_status.setSelected(false);
                                JOptionPane.showMessageDialog(rootPane, "El usuario creado correctamente. ");
                            } else {
                                JOptionPane.showMessageDialog(rootPane, "El usuario no se pudo crear, contacte al administrador. ");
                            }
                            break;
                        case 1:
                            user.setId(id);
                            if (UserDBController.setUser(user)) {

                                btnNuevo.setEnabled(true);
                                btnEditar.setEnabled(true);
                                btnBorrar.setEnabled(true);
                                btnBuscar.setEnabled(true);
                                btnGuardar.setEnabled(false);
                                btnCancelar.setEnabled(false);

                                //deshabilita los campos
                                txtIdUsuario.setEnabled(false);
                                txtIdUsuario.setText("");
                                txtNombres.setEnabled(false);
                                txtNombres.setText("");
                                txtApellidos.setEnabled(false);
                                txtApellidos.setText("");
                                txtClave.setEnabled(false);
                                txtClave.setText("");
                                txtConfirClave.setEnabled(false);
                                txtConfirClave.setText("");
                                txtIdUsuario.setEnabled(false);
                                txtIdUsuario.setText("");
                                cmbPerfil.setEnabled(false);
                                cmbPerfil.setSelectedIndex(0);
                                chk_status.setSelected(false);
                                JOptionPane.showMessageDialog(rootPane, "El usuario creado correctamente. ");
                            } else {
                                JOptionPane.showMessageDialog(rootPane, "El usuario no se pudo crear, contacte al administrador. ");
                            }
                            break;
                    }

                } else {
                    JOptionPane.showMessageDialog(rootPane, "La contrase√±a y la confirmacion no coinciden.");
                }
            }

        }


    }//GEN-LAST:event_btnGuardarActionPerformed

